function soma(num1,num2)
{
    return num1 + num2;
}

function subtração(num3,num4)
{
    return num3 - num4;
}

function multiplicação(num5,num6)
{
    return num5 * num6;
}

function divisão(num7,num8)
{
    return num7 / num8;
}

function resto(num9,num10)
{
    return num9 % num10;
}

function potenciação(num11,num12)
{
    return num11 ** num12;
}

exports.soma=soma 
exports.subtração=subtração
exports.multiplicação=multiplicação
exports.divisão=divisão
exports.resto=resto
exports.potenciação=potenciação

